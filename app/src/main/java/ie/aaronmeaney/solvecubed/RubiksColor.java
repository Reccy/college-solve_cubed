package ie.aaronmeaney.solvecubed;

/**
 * Represents the different possible colors on a Rubik's Cube
 */
public enum RubiksColor {

    /**
     * Red tile
     */
    RED,

    /**
     * Green tile
     */
    GREEN,

    /**
     * Blue tile
     */
    BLUE,

    /**
     * Yellow tile
     */
    YELLOW,

    /**
     * Orange tile
     */
    ORANGE,

    /**
     * White tile
     */
    WHITE}
